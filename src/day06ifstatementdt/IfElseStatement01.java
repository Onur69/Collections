package day06ifstatementdt;

public class IfElseStatement01 {

	public static void main(String[] args) {
		

		int num=3;
		//if -else statement te ya if ya else bolumu calisir ikisi birden asla calismaz
		//condition true ise if bolumu calisir, conditions false ise else kismi calisir
		if(num<0) {
			System.out.println("Negatif");
			
		}
		else {
			System.out.println("Negatif degil");
			
		}
		
	}

}
